#include "libft.h"

void	ft_putnbr(int i)
{
	if (i <= 9 && i >= 0)
	{
		ft_putchar(i + 48);
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	if (i < 0)
	{
		ft_putchar('-');
		if (i < -9)
		{
			ft_putnbr((0 - (unsigned int)i) / 10);
		}
		ft_putchar(((i % 10) * -1) + 48);
	}
}
