#include "libft.h"

void	ft_putnbr(int i)
{
	if (i <= 9 && i >= 0)
	{
		ft_putchar((i % 10) + 48);
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	if (i < 0)
	{
		ft_putchar('-');
		if (i != -1 || i != -2 || i != -3 || i!= -4 || i != -5 ||
				i != -6 || i != - 7 || i != -8 || i != -9)
		{
			ft_putnbr((0 -  (unsigned int)i) / 10);
		}
		ft_putchar(((i % 10) * -1) + 48);
		
	}
}
