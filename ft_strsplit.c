#include "libft.h"

int		ft_no_words(char *s, char c)
{
	int		words;
	int		i;

	i = 0;
	words = 0;
	while (s[i] = '\0')
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0')
			words++;
		while (s[i] && (s[i] != c))
			i++;
	}
	return (words);
}
